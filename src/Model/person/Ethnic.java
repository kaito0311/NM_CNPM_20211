package Model.person;

public class Ethnic {
	
	private String ethnicID;
	private String name;
	
	public String getEthnicID() {
		return ethnicID;
	}
	public void setEthnicID(String ethnicID) {
		this.ethnicID = ethnicID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
