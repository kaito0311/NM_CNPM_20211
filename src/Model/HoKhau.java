package Model;

import java.util.Date;

public class HoKhau {
    private String maHoKhau; 
    private String diaChiThanhPho, diaChiHuyen, diaChiXa, diaChiDuong, diaChiSoNha; 
    private Date ngayLap;
    public String getMaHoKhau() {
        return maHoKhau;
    }
    public void setMaHoKhau(String maHoKhau) {
        this.maHoKhau = maHoKhau;
    }
    public String getDiaChiThanhPho() {
        return diaChiThanhPho;
    }
    public void setDiaChiThanhPho(String diaChiThanhPho) {
        this.diaChiThanhPho = diaChiThanhPho;
    }
    public String getDiaChiHuyen() {
        return diaChiHuyen;
    }
    public void setDiaChiHuyen(String diaChiHuyen) {
        this.diaChiHuyen = diaChiHuyen;
    }
    public String getDiaChiXa() {
        return diaChiXa;
    }
    public void setDiaChiXa(String diaChiXa) {
        this.diaChiXa = diaChiXa;
    }
    public String getDiaChiDuong() {
        return diaChiDuong;
    }
    public void setDiaChiDuong(String diaChiDuong) {
        this.diaChiDuong = diaChiDuong;
    }
    public String getDiaChiSoNha() {
        return diaChiSoNha;
    }
    public void setDiaChiSoNha(String diaChiSoNha) {
        this.diaChiSoNha = diaChiSoNha;
    }
    public Date getNgayLap() {
        return ngayLap;
    }
    public void setNgayLap(Date ngayLap) {
        this.ngayLap = ngayLap;
    }

}
